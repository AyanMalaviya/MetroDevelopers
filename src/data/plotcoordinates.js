// Helper function to get plot center
export const getPlotCenter = (plotNumber) => {
  return plotCoordinates[plotNumber]?.center || { x: 0, y: 0 };
};

// Helper function to get all plots in a section
export const getPlotsBySection = (section) => {
  return Object.entries(plotCoordinates)
    .filter(([_, data]) => data.section === section)
    .map(([number, data]) => ({ number, ...data }));
};

